const LANG_STORAGE_KEY = "cotuong.lang";

function detectInitialLang() {
  try {
    const saved = localStorage.getItem(LANG_STORAGE_KEY);
    if (saved && STRINGS[saved]) return saved;
  } catch (_) { /* ignore */ }
  const nav = (navigator.language || "en").toLowerCase();
  return nav.startsWith("vi") ? "vi" : "en";
}

let lang = detectInitialLang();
const subscribers = new Set();

export function getLang() {
  return lang;
}

export function setLang(next) {
  if (!STRINGS[next] || next === lang) return;
  lang = next;
  try {
    localStorage.setItem(LANG_STORAGE_KEY, lang);
  } catch (_) { /* ignore */ }
  for (const fn of subscribers) {
    try { fn(lang); }
    catch (e) { console.warn("lang subscriber failed:", e); }
  }
}

export function onLangChange(fn) {
  subscribers.add(fn);
  return () => subscribers.delete(fn);
}

export function t(key) {
  return (STRINGS[lang] && STRINGS[lang][key]) || STRINGS.en[key] || key;
}

// Apply current language to any element with a [data-i18n] attribute, and
// reflect the active language on #lang-switch buttons.
export function applyLangToDom(root = document) {
  if (root === document) {
    document.documentElement.lang = lang;
    document.title = t("title");
  }
  for (const el of root.querySelectorAll("[data-i18n]")) {
    el.textContent = t(el.dataset.i18n);
  }
  for (const opt of document.querySelectorAll("#lang-toggle .lang-opt")) {
    opt.classList.toggle("active", opt.dataset.lang === lang);
  }
}
